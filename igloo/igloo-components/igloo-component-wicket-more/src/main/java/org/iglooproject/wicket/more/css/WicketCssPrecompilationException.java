package org.iglooproject.wicket.more.css;

public class WicketCssPrecompilationException extends RuntimeException {

  private static final long serialVersionUID = -1761121529431529383L;

  public WicketCssPrecompilationException() {
    super();
  }

  public WicketCssPrecompilationException(String message, Throwable cause) {
    super(message, cause);
  }

  public WicketCssPrecompilationException(String message) {
    super(message);
  }

  public WicketCssPrecompilationException(Throwable cause) {
    super(cause);
  }
}
